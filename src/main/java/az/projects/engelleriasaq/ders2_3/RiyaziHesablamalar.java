public class RiyaziHesablamalar {
    
    public static void main(String[] args) {
        int a=5;
        int b=9;
        
        //toplama əməliyyatı
        System.out.println("a+b="+(a+b));
        
        //çıxma əməliyyatı
        System.out.println("a-b="+(a-b));
        
        //vurma əməliyyatı
        System.out.println("a*b="+(a*b));
        
        //bölmə əməliyyatı
        System.out.println("a/b="+(a/b));
        
        //modular bölmə əməliyyatı
        System.out.println("a%b="+(a%b));
    }
    
}
